class user {
   /**
    * This method adds two numbers
    * @param a
    * @param b
    * @return sum of a and b
    */
    public static int add(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Addition of 2 and 3 is: " + add(2, 3));
    }
}